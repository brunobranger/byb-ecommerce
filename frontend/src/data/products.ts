const generateSlug = (name: string): string =>
    name
        .toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '') // saca tildes
        .replace(/[^a-z0-9\s-]/g, '') // saca caracteres especiales
        .trim()
        .replace(/\s+/g, '-') // espacios → guiones

